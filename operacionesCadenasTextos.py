#  Convertir a minusculas
cadena = "EJempLo dE cADenA DE TexTOs"
print(cadena.lower())
#Convertir a mayuscula
print(cadena.upper())
#Capital(tipo oracion)
print(cadena.capitalize())
#titulo(Cada palabra empieza con mayuscula)
print(cadena.title())
#invertir(donde habia mayusculas por minusculas y viceversa)
print(cadena.swapcase())
print(cadena)
#esta en mayuscula?
print(cadena.isupper())
#esta en minuscula?
print(cadena.islower())
#es numerica?
print(cadena.isnumeric())
#es alfanumerica?
print(cadena.isalnum())
cadena = "Titulo 1"
print(cadena.istitle())
cadena = "Esto no es titulo"
